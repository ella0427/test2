#include <stdio.h>

int main(){
     char char_neg=-128;
     char char_pos=127;

     printf("char_neg: %d\n", char_neg);
     printf("char_pos: %d\n", char_pos);

     unsigned char uns_char_neg=-1;
     unsigned char uns_char_pos=255;

     printf("char_neg: %u\n", uns_char_neg);
     printf("char_pos: %u\n", uns_char_pos);
return 0;
}
